/**
 * Represents a single hadith entry.
 *
 * @param id         Unique stable identifier — never reorder or reuse.
 * @param text       Full Arabic hadith text.
 * @param narrator   Companion who narrated the hadith.
 * @param source     Primary book reference.
 * @param bookNumber Hadith number within the source book, or null if not applicable.
 * @param grade      Authenticity grade: "صحيح" | "حسن" | "حسن صحيح".
 * @param category   [HadithCategory] this hadith belongs to.
 * @param benefit    One-line Arabic benefit / فائدة shown in the card subtitle.
 * @param nawawi     Hadith number in الأربعين النووية, or null if not from that collection.
 */
data class Hadith(
    val id         : Int,
    val text       : String,
    val narrator   : String,
    val source     : String,
    val bookNumber : String?,
    val grade      : String,
    val category   : HadithCategory,
    val benefit    : String,
    val nawawi     : Int? = null
)

/**
 * Top-level categories shown as filter chips in IslamicScreen.
 */
enum class HadithCategory(val label: String, val emoji: String) {
    INTENTION      ("النية والإخلاص",         "🤍"),
    AQEEDAH        ("العقيدة والأركان",        "⭐"),
    DHIKR_DUA      ("الذكر والدعاء",           "🤲"),
    PRAYER         ("الصلاة",                  "🕌"),
    FASTING        ("الصيام",                  "🌙"),
    MANNERS        ("المعاملات والأخلاق",       "🌿"),
    KNOWLEDGE      ("فضل العلم",               "📖"),
    TAWBAH         ("التوبة والاستغفار",        "💧"),
    PARADISE_HELL  ("الجنة والنار",             "✨"),
    ZUHD           ("الزهد والدنيا",            "🍂");

    companion object {
        val ALL_LABEL = "الكل"
    }
}

