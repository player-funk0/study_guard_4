/** Returns all hadiths belonging to [category], preserving insertion order. */
fun hadithsByCategory(category: HadithCategory): List<Hadith> =
    ALL_HADITHS.filter { it.category == category }

/** Returns only the Forty Nawawi hadiths in order. */
fun nawawiHadiths(): List<Hadith> =
    ALL_HADITHS.filter { it.nawawi != null }.sortedBy { it.nawawi }

/** Returns hadiths whose text or benefit or narrator contains [query] (case-insensitive). */
fun searchHadiths(query: String): List<Hadith> {
    if (query.isBlank()) return ALL_HADITHS
    return ALL_HADITHS.filter { hadith ->
        hadith.text.contains(query, ignoreCase = true) ||
        hadith.benefit.contains(query, ignoreCase = true) ||
        hadith.narrator.contains(query, ignoreCase = true) ||
        hadith.source.contains(query, ignoreCase = true)
    }
}

/**
 * Returns a "hadith of the day" that changes daily.
 *
 * Seed combines year × 1000 + dayOfYear so:
 *  - Different days within the same year map to different hadiths.
 *  - The sequence shifts every year so the same date doesn't repeat the same hadith.
 *  - With 60 hadiths, every hadith appears roughly every 60 days.
 */
fun dailyHadith(): Hadith {
    val calendar   = java.util.Calendar.getInstance()
    val year       = calendar.get(java.util.Calendar.YEAR)
    val dayOfYear  = calendar.get(java.util.Calendar.DAY_OF_YEAR)
    val seed       = (year * 1000 + dayOfYear) % ALL_HADITHS.size
    return ALL_HADITHS[seed]
}
